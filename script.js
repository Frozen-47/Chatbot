            initializeBot() {
                this.startBootSequence();
            }

            startBootSequence() {
                const bootMessages = [
                    { text: "‚úì Initializing Teacher Appreciation System...", type: "success", delay: 1000 },
                    { text: "‚úì Loading Educational Database...", type: "success", delay: 1500 },
                    { text: "‚úì Connecting to Wisdom Network...", type: "info", delay: 2000 },
                    { text: "‚ö° Activating AI Learning Modules...", type: "warning", delay: 2500 },
                    { text: "‚úì System Ready - Teacher's Day Mode Active", type: "success", delay: 3000 }
                ];

                const bootMessagesContainer = document.getElementById('bootMessages');
                
                bootMessages.forEach((message, index) => {
                    setTimeout(() => {
                        const messageEl = document.createElement('div');
                        messageEl.className = `boot-message ${message.type}`;
                        messageEl.textContent = message.text;
                        bootMessagesContainer.appendChild(messageEl);
                        
                        setTimeout(() => {
                            messageEl.classList.add('show');
                        }, 100);
                        
                        // If this is the last message, start transition to main interface
                        if (index === bootMessages.length - 1) {
                            setTimeout(() => {
                                this.completeBootSequence();
                            }, 1500);
                        }
                    }, message.delay);
                });
            }

            completeBootSequence() {
                const bootScreen = document.getElementById('bootScreen');
                const terminalWindow = document.getElementById('terminalWindow');
                
                // Fade out boot screen
                bootScreen.classList.add('hidden');
                
                // Show terminal window
                setTimeout(() => {
                    terminalWindow.classList.add('show');
                    this.setupEventListeners();
                    this.messageInput.focus();
                    
                    // Add welcome system message
                    setTimeout(() => {
                        this.addBotMessage("üéì System initialized successfully! Teacher Appreciation Mode is now active. Ready to celebrate educators!");
                    }, 500);
                }, 500);
            }

            setupEventListeners() {
                this.messageInput.addEventListener('keydown', (event) => {
                    if (event.key === 'Enter') {
                        this.sendMessage();
                    }
                });
            }

            sendMessage() {
                const message = this.messageInput.value.trim();
                if (!message) return;

                this.addUserMessage(message);
                this.messageInput.value = '';
                
                setTimeout(() => {
                    this.processMessage(message);
                }, 500);
            }

            addUserMessage(message) {
                const messageDiv = document.createElement('div');
                messageDiv.className = 'chat-message user-message';
                messageDiv.innerHTML = `<span>${this.escapeHtml(message)}</span>`;
                this.chatArea.appendChild(messageDiv);
                this.scrollToBottom();
            }

            addBotMessage(message) {
                const messageDiv = document.createElement('div');
                messageDiv.className = 'chat-message bot-message';
                messageDiv.innerHTML = `<span>${this.escapeHtml(message)}</span>`;
                this.chatArea.appendChild(messageDiv);
                this.scrollToBottom();
            }

            addTypingIndicator() {
                const typingDiv = document.createElement('div');
                typingDiv.className = 'chat-message bot-message typing-indicator';
                typingDiv.innerHTML = '<span>EduBot is typing...</span>';
                typingDiv.id = 'typing-indicator';
                this.chatArea.appendChild(typingDiv);
                this.scrollToBottom();
                return typingDiv;
            }

            removeTypingIndicator() {
                const typingIndicator = document.getElementById('typing-indicator');
                if (typingIndicator) {
                    typingIndicator.remove();
                }
            }

            processMessage(message) {
                const typingIndicator = this.addTypingIndicator();
                
                setTimeout(() => {
                    this.removeTypingIndicator();
                    const response = this.getResponse(message);
                    this.addBotMessage(response);
                }, 1000 + Math.random() * 1000);
            }

